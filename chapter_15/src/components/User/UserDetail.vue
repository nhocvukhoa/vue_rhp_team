<template>
    <div>
        <h1>UserDetail</h1>
        <p>ID User: {{ id }}</p>
    </div>
</template>

<script>
    export default {
        data() {
            return {
                id: this.$route.params.id
            }
        },
        watch: {
            '$route'(to, from) {
                this.id = to.params.id;
            }
        }
    }
</script>

